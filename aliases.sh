
# This is a collection of aliases that I find helpful

########### NAVIGATION ###########
# Go to previous dir
alias cdc="cd -"

# Go up one dir
alias cx="cd .."

# Go up two dir
alias cxx="cd ../.."


########### RC FILE #############
alias zshrc="vi ~/.zshrc"
alias zshrcl="source ~/.zshrc"

###########  GIT  ###############
alias gitcom="git commit"
alias gitam="git commit --amend"
alias gl="git log"
alias gs="git status"

########### TOOLS ###############

alias jsonp="python -m json.tool"
